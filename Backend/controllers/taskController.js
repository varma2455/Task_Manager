// ===============================
// Search Tasks
// GET /api/tasks/search?keyword=abc
// ===============================
export const searchTasks = async (req, res) => {
  try {
    const keyword = req.query.keyword || "";

    const tasks = await Task.find({
      user: req.user._id,
      title: {
        $regex: keyword,
        $options: "i",
      },
    });

    res.status(200).json({
      success: true,
      count: tasks.length,
      tasks,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

// ===============================
// Filter Tasks
// GET /api/tasks/filter?status=Pending
// ===============================
export const filterTasks = async (req, res) => {
  try {
    const { status, priority } = req.query;

    const filter = {
      user: req.user._id,
    };

    if (status) {
      filter.status = status;
    }

    if (priority) {
      filter.priority = priority;
    }

    const tasks = await Task.find(filter);

    res.status(200).json({
      success: true,
      count: tasks.length,
      tasks,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};